    parser.add_argument("--weight_decay", type=float, default=1e-2)
    parser.add_argument("--num_workers", type=int, default=4)
    parser.add_argument("--grad_accum", type=int, default=1)
    parser.add_argument("--save_every", type=int, default=5000)
    parser.add_argument("--force_retrain", action="store_true")
    
    # Wandb
    parser.add_argument("--wandb_project", type=str, default=None)
    parser.add_argument("--wandb_run_name", type=str, default=None)
    parser.add_argument("--wandb_entity", type=str, default=None)
    parser.add_argument("--no_wandb", action="store_true")

    parser.add_argument("--monitor_every", type=int, default=1000)
    parser.add_argument("--monitor_activations", action="store_true")
    parser.add_argument("--compile", action="store_true")

    args = parser.parse_args()

    # Save path
    if not os.path.exists(args.ckpt_dir):
        os.makedirs(args.ckpt_dir, exist_ok=True)
        
    save_path = os.path.join(args.ckpt_dir, "dynamics_pusht.pt")
    if os.path.exists(save_path) and not args.force_retrain:
        print(f"Model already trained at {save_path}. Use --force_retrain to override.")
        return

    device = torch.device("cuda" if torch.cuda.is_available() else "cpu")
    print(f"Using device: {device}")
    
