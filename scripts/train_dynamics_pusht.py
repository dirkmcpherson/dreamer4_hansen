def save_ckpt(path: Path, *, step: int, dyn_model, opt, scaler, args: argparse.Namespace):
    path = Path(path)
    path.parent.mkdir(parents=True, exist_ok=True)
    obj = {
        "step": step,
        "dynamics": (dyn_model.module.state_dict() if hasattr(dyn_model, "module") else dyn_model.state_dict()),
        "opt": opt.state_dict(),
        "scaler": scaler.state_dict() if scaler is not None else None,
        "args": vars(args),
    }
    tmp = path.with_suffix(".tmp")
    torch.save(obj, tmp)
    tmp.replace(path)

def load_ckpt(path: Path, *, dyn_model, opt, scaler) -> int:
    path = Path(path)
    if not path.exists():
        return 0
    ckpt = torch.load(path, map_location="cpu")
    state = ckpt["dynamics"]
    (dyn_model.module if hasattr(dyn_model, "module") else dyn_model).load_state_dict(state, strict=True)
    if opt is not None and "opt" in ckpt:
        opt.load_state_dict(ckpt["opt"])
    if scaler is not None and ckpt.get("scaler") is not None:
        scaler.load_state_dict(ckpt["scaler"])
    return int(ckpt.get("step", 0))

def seed_everything(seed: int):
    random.seed(seed)
    np.random.seed(seed)
    torch.manual_seed(seed)
    if torch.cuda.is_available():
        torch.cuda.manual_seed_all(seed)

class PushTShardedDataset(Dataset):
    """
    Custom dataset for PushT shards generated by generate_pusht_dataset.py.
    Each shard contains: frames, actions, rewards, is_terminal, is_first.
    """
    def __init__(self, outdirs, seq_len=16):
        self.seq_len = seq_len
        self.outdirs = [outdirs] if isinstance(outdirs, str) else outdirs
        self.shards = []
        
        # Discover shards
        for d in self.outdirs:
            # Assuming structure: data_dir/pusht/*.pt or data_dir/*.pt
            # generate_pusht_dataset.py outputs to out_dir/pusht/shard_XXXX.pt
            # User passes out_dir usually.
            search_paths = [
                os.path.join(d, "pusht", "*.pt"),
                os.path.join(d, "*.pt")
            ]
            for p in search_paths:
                found = sorted(glob.glob(p))
                if found:
                    self.shards.extend(found)
                    break
        
        if not self.shards:
            print(f"Warning: No shards found in {self.outdirs}")

        self.index = []
        self.cache = {} # Simple cache
        
        # Build index (shard_idx, start_idx)
        # To avoid loading all shards to count frames, we assume they are valid or load lazily.
        # But for __len__ we need counts.
        # Let's verify a few or trust generate script produced good shards.
        # For speed, let's just load them if count is small (<100).
        
        print(f"Indexing {len(self.shards)} shards...")
        for shard_idx, p in enumerate(self.shards):
            try:
                # We need to know length. 
                # Optimization: save metadata in filename or separate file?
                # or just load header? torch.load loads whole file typically.
                # For now load it.
                data = torch.load(p, map_location="cpu")
                N = data["frames"].shape[0]
                if N > seq_len:
                    # We can start from 0 to N - seq_len
                    # We accept overlapping windows? Yes.
                    num_starts = N - seq_len
                    for i in range(num_starts):
                        self.index.append((shard_idx, i))
                        
                # Cache small number of shards in memory if possible?
                # Only if memory allows. Let's not cache eagerly.
            except Exception as e:
                print(f"Error loading shard {p}: {e}")
        
        print(f"dataset len: {len(self.index)}")

    def get_source_counts(self):
        counts = {}
        for shard_idx, _ in self.index:
            path = self.shards[shard_idx]
            # Assumes path like .../source/shard_XXXX.pt or .../source/pusht/shard_XXXX.pt
            # We want the folder name that distinguish train vs play
            parts = path.split(os.sep)
            # If path is data/pusht_train/pusht/shard_0000.pt, parts[-3] is pusht_train
            if "pusht" in parts[-2]:
                source = parts[-3]
            else:
                source = parts[-2]
            counts[source] = counts.get(source, 0) + 1
        return counts

    def __len__(self):
        return len(self.index)
    
    def __getitem__(self, idx):
        shard_idx, start = self.index[idx]
        path = self.shards[shard_idx]
        
        # Load shard (cached?)
        if path in self.cache:
            data = self.cache[path]
        else:
            data = torch.load(path, map_location="cpu")
            # Simple LRU or just cache all if small dataset (PushT is small)
            if len(self.cache) < 20: 
                self.cache[path] = data
        
        # Get window
        end = start + self.seq_len # returns T frames
        # Usually dynamics needs T+1 frames? 
        # dynamics_pretrain_loss takes z1 (targets).
        # We usually predict next latent.
        # If we have T frames, we can self-supervise T-1 transitions?
        # dreamer4 usually takes sequence of length T.
        
        frames = data["frames"][start:end].float() / 255.0 # (T, 3, H, W)
        actions = data["actions"][start:end].float() # (T, A)
        rewards = data["rewards"][start:end].float() # (T)
        
        # Handle Act Mask? PushT doesn't have it, assume all 1s
        act_mask = torch.ones_like(actions) 
        
        return {
            "frames": frames,
            "actions": actions,
            "rewards": rewards,
            "act_mask": act_mask
        }

def main():
    parser = argparse.ArgumentParser()
    default_data_dir = os.path.join(os.path.dirname(__file__), "../data/pusht_train")
    parser.add_argument("--data_dirs", type=str, nargs="+", default=[default_data_dir])
    parser.add_argument("--tokenizer_ckpt", type=str, default="tok_latest.pt")
    parser.add_argument("--max_steps", type=int, default=200000)
    parser.add_argument("--batch_size", type=int, default=4)
    parser.add_argument("--ckpt_dir", type=str, default=".")
    
    # Dynamics Config
    parser.add_argument("--d_model", type=int, default=256)
    parser.add_argument("--depth", type=int, default=4)
    parser.add_argument("--n_heads", type=int, default=4)
    parser.add_argument("--k_max", type=int, default=16)
    parser.add_argument("--packing_factor", type=int, default=1)
